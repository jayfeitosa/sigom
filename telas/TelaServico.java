    private void imprimir_servico() {
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão deste serviço?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            try {
                HashMap filtro = new HashMap();
                filtro.put("id", Integer.parseInt(txtOs.getText()));
                JasperPrint print = JasperFillManager.fillReport(getClass().getResourceAsStream("/reports/os.jasper"), filtro, conexao);
                JasperViewer.viewReport(print, false);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

    private void recuperar_servico() {
        String sql = "select max(id) from tbservico";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                txtOs.setText(rs.getString(1));
            }
          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
